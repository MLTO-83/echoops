#!/usr/bin/env node

/**
 * Test script for the new 6-step AI job flow with improved error handling
 *
 * Steps:
 * 1. Prompt AI provider for code
 * 2. AI response received
 * 3. Get repository info
 * 4. Create feature branch
 * 5. Push code to feature branch
 * 6. Create pull request
 */

const axios = require("axios");
const {
  createAzureDevOpsIntegration,
  generateBranchName,
} = require("./azure-devops-integration.js");

// Test configuration
const TEST_CONFIG = {
  // Set to true to test with real Azure DevOps APIs, false for mock mode
  USE_REAL_API: false,

  // Azure DevOps test data (when USE_REAL_API is true)
  ADO_ORGANIZATION_URL:
    process.env.ADO_ORGANIZATION_URL || "https://dev.azure.com/torslev",
  ADO_PAT: process.env.ADO_PAT || "",
  ADO_PROJECT_ID:
    process.env.ADO_PROJECT_ID || "f4f6c3b8-8c0a-4e5d-9b2a-1234567890ab",
  REPOSITORY_NAME: process.env.REPOSITORY_NAME || "MasterData management",

  // Test data
  MOCK_GENERATED_CODE: `# AI Generated Code - Test

## Overview
This is a test implementation generated by AI for testing purposes.

## Features
- Feature 1: Basic functionality
- Feature 2: Enhanced error handling
- Feature 3: Comprehensive logging

## Usage
\`\`\`javascript
console.log("Hello from AI generated code!");
\`\`\`

## Notes
This code was generated as part of the 6-step AI job flow testing process.
`,
};

// Logging utility
function log(message) {
  const timestamp = new Date().toISOString();
  console.log(`${timestamp} - ${message}`);
}

/**
 * STEP 1: Mock AI provider for code generation
 */
async function mockPromptAIProvider() {
  log("Step 1: Prompting AI Provider (Mock)");

  // Simulate AI processing time
  await new Promise((resolve) => setTimeout(resolve, 1000));

  const generatedCode = TEST_CONFIG.MOCK_GENERATED_CODE;
  log(`Generated code length: ${generatedCode.length} characters`);

  return generatedCode;
}

/**
 * STEP 2: Get AI provider response (completed in step 1)
 */
async function mockGetAIProviderResponse() {
  log("Step 2: AI Response Received (Mock)");
  return "AI response processing complete";
}

/**
 * STEP 3: Get repository info using Azure DevOps integration
 */
async function getRepositoryInfo(adoIntegration, repositoryName) {
  log(`Step 3: Getting repository info for ${repositoryName}`);

  try {
    const lookupResult = await adoIntegration.lookupRepository(repositoryName);
    const { repository, defaultBranch, needsInitialization } = lookupResult;

    log(`Repository found: ${repository.name} (ID: ${repository.id})`);
    log(`Default branch: ${defaultBranch?.name || "None"}`);
    log(`Needs initialization: ${needsInitialization}`);

    // Initialize repository if needed
    let currentDefaultBranch = defaultBranch;
    if (needsInitialization) {
      log(`Initializing repository ${repository.name}`);
      currentDefaultBranch = await adoIntegration.initializeRepository(
        repository.id,
        repository.name
      );
      log(`Repository initialized with branch: ${currentDefaultBranch.name}`);
    }

    if (!currentDefaultBranch) {
      throw new Error("Unable to determine or create default branch");
    }

    return {
      adoIntegration,
      repository,
      repositoryName: repository.name,
      repositoryId: repository.id,
      mainBranchRef: currentDefaultBranch.name,
      mainBranchObjectId: currentDefaultBranch.objectId,
      defaultBranch: currentDefaultBranch,
    };
  } catch (error) {
    log(`Error in Step 3: ${error.message}`);
    throw error;
  }
}

/**
 * STEP 4: Create feature branch
 */
async function createFeatureBranch(repositoryInfo, branchName) {
  log(`Step 4: Creating feature branch ${branchName}`);

  const { adoIntegration, repository, defaultBranch } = repositoryInfo;

  try {
    const branchResult = await adoIntegration.createFeatureBranch(
      repository.id,
      branchName,
      defaultBranch
    );

    log(`Feature branch ${branchName} created successfully`);
    log(`Branch commit ID: ${branchResult.commitId}`);

    return {
      ...repositoryInfo,
      branchName,
      branchResult,
    };
  } catch (error) {
    log(`Error in Step 4: ${error.message}`);
    throw error;
  }
}

/**
 * STEP 5: Push code to feature branch
 */
async function pushCodeToFeatureBranch(branchInfo, generatedCode) {
  log(
    `Step 5: Pushing generated code to feature branch ${branchInfo.branchName}`
  );

  const { adoIntegration, repository, branchName, branchResult } = branchInfo;

  try {
    const fileName = "ai-generated-code.md";
    const newCommitId = await adoIntegration.pushCodeToFeatureBranch(
      repository.id,
      branchName,
      branchResult.commitId,
      generatedCode,
      fileName
    );

    log(`Code pushed to branch ${branchName}, new commit: ${newCommitId}`);

    return {
      ...branchInfo,
      newCommitId,
      fileName,
    };
  } catch (error) {
    log(`Error in Step 5: ${error.message}`);
    throw error;
  }
}

/**
 * STEP 6: Create pull request
 */
async function createPullRequest(codeInfo) {
  log(`Step 6: Creating pull request for branch ${codeInfo.branchName}`);

  const { adoIntegration, repository, branchName, mainBranchRef } = codeInfo;

  try {
    const pullRequestResult = await adoIntegration.createPullRequest(
      repository.id,
      repository.name,
      branchName,
      mainBranchRef
    );

    log(`Pull request created successfully: ${pullRequestResult.url}`);
    return pullRequestResult.url;
  } catch (error) {
    log(`Error in Step 6: ${error.message}`);
    throw error;
  }
}

/**
 * Mock Azure DevOps Integration for testing
 */
class MockAzureDevOpsIntegration {
  constructor() {
    this.mockData = {
      repository: {
        id: "mock-repo-id-12345",
        name: TEST_CONFIG.REPOSITORY_NAME,
      },
      defaultBranch: {
        name: "refs/heads/main",
        objectId: "abc123def456789012345678901234567890abcd",
      },
    };
  }

  async lookupRepository(repositoryName) {
    log(`[MOCK] Looking up repository: ${repositoryName}`);
    await new Promise((resolve) => setTimeout(resolve, 500));

    return {
      repository: this.mockData.repository,
      defaultBranch: this.mockData.defaultBranch,
      needsInitialization: false,
      hasReadme: true,
    };
  }

  async createFeatureBranch(repositoryId, branchName, baseBranch) {
    log(`[MOCK] Creating feature branch: ${branchName}`);
    await new Promise((resolve) => setTimeout(resolve, 700));

    return {
      branchName,
      branchRef: {
        name: `refs/heads/${branchName}`,
        objectId: baseBranch.objectId,
      },
      commitId: baseBranch.objectId,
    };
  }

  async pushCodeToFeatureBranch(
    repositoryId,
    branchName,
    baseCommitId,
    generatedCode,
    fileName
  ) {
    log(`[MOCK] Pushing code to feature branch: ${branchName}`);
    log(`[MOCK] File: ${fileName}, Code length: ${generatedCode.length} chars`);
    await new Promise((resolve) => setTimeout(resolve, 800));

    const newCommitId = "def456abc789012345678901234567890abcdef1";
    return newCommitId;
  }

  async createPullRequest(
    repositoryId,
    repositoryName,
    sourceBranch,
    targetBranch
  ) {
    log(`[MOCK] Creating pull request from ${sourceBranch} to ${targetBranch}`);
    await new Promise((resolve) => setTimeout(resolve, 600));

    const prId = Math.floor(Math.random() * 1000) + 1;
    const prUrl = `https://dev.azure.com/torslev/${encodeURIComponent(repositoryName)}/_git/${encodeURIComponent(repositoryName)}/pullrequest/${prId}`;

    return {
      pullRequest: { pullRequestId: prId },
      url: prUrl,
    };
  }
}

/**
 * Main test function
 */
async function testSixStepAIJobFlow() {
  log("=== Starting 6-Step AI Job Flow Test ===");
  log(`Mode: ${TEST_CONFIG.USE_REAL_API ? "REAL API" : "MOCK"}`);

  try {
    // Generate unique branch name
    const branchName = generateBranchName("test-6-step");
    log(`Generated branch name: ${branchName}`);

    // STEP 1: Prompt AI provider for code
    log(`\n--- STEP 1: Prompting AI Provider ---`);
    const generatedCode = await mockPromptAIProvider();
    log(`Generated code ready`);

    // STEP 2: Get AI provider response
    log(`\n--- STEP 2: AI Response Received ---`);
    await mockGetAIProviderResponse();

    // Initialize Azure DevOps integration
    let adoIntegration;
    if (TEST_CONFIG.USE_REAL_API) {
      if (!TEST_CONFIG.ADO_PAT) {
        throw new Error(
          "ADO_PAT environment variable required for real API testing"
        );
      }

      const adoApi = axios.create({
        baseURL: TEST_CONFIG.ADO_ORGANIZATION_URL,
        headers: {
          Authorization: `Basic ${Buffer.from(`:${TEST_CONFIG.ADO_PAT}`).toString("base64")}`,
          "Content-Type": "application/json",
        },
      });

      adoIntegration = createAzureDevOpsIntegration(
        adoApi,
        TEST_CONFIG.ADO_PROJECT_ID,
        TEST_CONFIG.ADO_ORGANIZATION_URL
      );
    } else {
      adoIntegration = new MockAzureDevOpsIntegration();
    }

    // STEP 3: Get repository info
    log(`\n--- STEP 3: Getting Repository Info ---`);
    const repositoryInfo = await getRepositoryInfo(
      adoIntegration,
      TEST_CONFIG.REPOSITORY_NAME
    );
    log(`Repository info retrieved successfully`);

    // STEP 4: Create feature branch
    log(`\n--- STEP 4: Creating Feature Branch ---`);
    const branchInfo = await createFeatureBranch(repositoryInfo, branchName);
    log(`Feature branch created successfully`);

    // STEP 5: Push code to feature branch
    log(`\n--- STEP 5: Pushing Code to Feature Branch ---`);
    const codeInfo = await pushCodeToFeatureBranch(branchInfo, generatedCode);
    log(`Code pushed to feature branch successfully`);

    // STEP 6: Create pull request
    log(`\n--- STEP 6: Creating Pull Request ---`);
    const prUrl = await createPullRequest(codeInfo);
    log(`Pull request created: ${prUrl}`);

    log(`\n=== ✅ 6-Step AI Job Flow Test COMPLETED SUCCESSFULLY ===`);
    log(`Branch: ${branchName}`);
    log(`PR URL: ${prUrl}`);

    return {
      success: true,
      branchName,
      prUrl,
      steps: 6,
    };
  } catch (error) {
    log(`\n=== ❌ 6-Step AI Job Flow Test FAILED ===`);
    log(`Error: ${error.message}`);

    return {
      success: false,
      error: error.message,
    };
  }
}

// Run the test if this script is executed directly
if (require.main === module) {
  testSixStepAIJobFlow()
    .then((result) => {
      console.log("\n=== TEST RESULT ===");
      console.log(JSON.stringify(result, null, 2));
      process.exit(result.success ? 0 : 1);
    })
    .catch((error) => {
      console.error("Test runner error:", error);
      process.exit(1);
    });
}

module.exports = {
  testSixStepAIJobFlow,
  TEST_CONFIG,
};
