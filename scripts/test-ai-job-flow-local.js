// Simplified Azure DevOps Integration (using the same logic as the main integration)
class MockAzureDevOpsIntegration {
  constructor(adoApi, projectId, organizationUrl) {
    this.adoApi = adoApi;
    this.projectId = projectId;
    this.organizationUrl = organizationUrl;
  }

  async lookupRepository(repositoryName) {
    try {
      log(
        `Looking up repository "${repositoryName}" in project ${this.projectId}`
      );

      const repositoriesResponse = await this.adoApi.get(
        `/${encodeURIComponent(this.projectId)}/_apis/git/repositories?api-version=7.0`
      );

      if (!repositoriesResponse.data?.value) {
        throw new Error(`No repositories found in project ${this.projectId}`);
      }

      const repositories = repositoriesResponse.data.value;
      log(`Found ${repositories.length} repositories in project`);

      // Enhanced repository matching logic (same as main implementation)
      let targetRepository = repositories.find(
        (repo) => repo.name === repositoryName
      );

      if (!targetRepository) {
        targetRepository = repositories.find(
          (repo) => repo.name.toLowerCase() === repositoryName.toLowerCase()
        );
      }

      if (!targetRepository) {
        targetRepository = repositories.find(
          (repo) =>
            repo.name.toLowerCase().includes(repositoryName.toLowerCase()) ||
            repositoryName.toLowerCase().includes(repo.name.toLowerCase())
        );
      }

      if (!targetRepository) {
        const availableRepos = repositories.map((r) => r.name).join(", ");
        throw new Error(
          `Repository "${repositoryName}" not found. Available: ${availableRepos}`
        );
      }

      log(
        `✅ Repository found: ${targetRepository.name} (ID: ${targetRepository.id})`
      );

      return {
        repository: targetRepository,
        matchType:
          targetRepository.name === repositoryName
            ? "exact"
            : targetRepository.name.toLowerCase() ===
                repositoryName.toLowerCase()
              ? "case-insensitive"
              : "partial",
      };
    } catch (error) {
      log(`❌ Repository lookup failed: ${error.message}`, "ERROR");
      throw error;
    }
  }

  async getDefaultBranch(repositoryId) {
    try {
      log(`Getting default branch for repository ${repositoryId}`);

      const refsResponse = await this.adoApi.get(
        `/${encodeURIComponent(this.projectId)}/_apis/git/repositories/${repositoryId}/refs?filter=heads/&api-version=7.0`
      );

      if (!refsResponse.data?.value || refsResponse.data.value.length === 0) {
        log("No branches found - repository is empty");
        return null;
      }

      const branches = refsResponse.data.value;
      log(`Found ${branches.length} branches`);

      let defaultBranch = branches.find(
        (ref) => ref.name === "refs/heads/main"
      );
      if (!defaultBranch) {
        defaultBranch = branches.find(
          (ref) => ref.name === "refs/heads/master"
        );
      }
      if (!defaultBranch) {
        defaultBranch = branches.find(
          (ref) =>
            ref.name.startsWith("refs/heads/") &&
            ref.objectId !== "0000000000000000000000000000000000000000"
        );
      }

      if (defaultBranch) {
        log(
          `✅ Default branch: ${defaultBranch.name} (${defaultBranch.objectId})`
        );
      } else {
        log("No valid default branch found");
      }

      return defaultBranch || null;
    } catch (error) {
      log(`❌ Failed to get default branch: ${error.message}`, "ERROR");
      return null;
    }
  }

  async createFeatureBranch(repositoryId, branchName, baseBranch) {
    try {
      log(`Creating feature branch ${branchName} from ${baseBranch.name}`);
      log(`Repository ID: ${repositoryId}`);
      log(`Base commit ID: ${baseBranch.objectId}`);
      log(`Project ID: ${this.projectId}`);

      // Try the correct API format - the refUpdates should be at the root level
      const requestPayload = [
        {
          name: `refs/heads/${branchName}`,
          oldObjectId: "0000000000000000000000000000000000000000",
          newObjectId: baseBranch.objectId,
        },
      ];

      const requestUrl = `/${encodeURIComponent(this.projectId)}/_apis/git/repositories/${repositoryId}/refs?api-version=6.0`;

      log(`Request URL: ${requestUrl}`);
      log(`Request payload: ${JSON.stringify(requestPayload, null, 2)}`);

      const createBranchResponse = await this.adoApi.post(
        requestUrl,
        requestPayload
      );

      if (!createBranchResponse.data?.value?.[0]) {
        throw new Error("Failed to create feature branch");
      }

      log(`✅ Feature branch ${branchName} created successfully`);
      return {
        branchName,
        branchRef: {
          name: `refs/heads/${branchName}`,
          objectId: baseBranch.objectId,
        },
        commitId: baseBranch.objectId,
      };
    } catch (error) {
      log(`❌ Feature branch creation failed: ${error.message}`, "ERROR");

      // Enhanced error logging for 400 errors
      if (error.response) {
        log(`Response status: ${error.response.status}`, "ERROR");
        log(`Response headers:`, "ERROR");
        console.log(JSON.stringify(error.response.headers, null, 2));
        log(`Response data:`, "ERROR");
        console.log(JSON.stringify(error.response.data, null, 2));

        if (error.response.status === 400) {
          log(`400 Bad Request Details:`, "ERROR");
          log(`- Branch name: ${branchName}`, "ERROR");
          log(`- Repository ID: ${repositoryId}`, "ERROR");
          log(`- Base commit: ${baseBranch.objectId}`, "ERROR");
          log(`- Project ID: ${this.projectId}`, "ERROR");
        }
      }

      throw error;
    }
  }

  async pushCodeToFeatureBranch(
    repositoryId,
    branchName,
    baseCommitId,
    generatedCode,
    fileName = "ai-generated-code.md"
  ) {
    try {
      log(`Pushing code to feature branch ${branchName}`);

      const pushResponse = await this.adoApi.post(
        `/${encodeURIComponent(this.projectId)}/_apis/git/repositories/${repositoryId}/pushes?api-version=7.0`,
        {
          refUpdates: [
            {
              name: `refs/heads/${branchName}`,
              oldObjectId: baseCommitId,
            },
          ],
          commits: [
            {
              comment: `Add AI generated code - ${fileName}`,
              changes: [
                {
                  changeType: "add",
                  item: { path: `/${fileName}` },
                  newContent: {
                    content: generatedCode,
                    contentType: "rawtext",
                  },
                },
              ],
            },
          ],
        }
      );

      if (!pushResponse.data?.commits?.[0]) {
        throw new Error("Failed to push code to feature branch");
      }

      const newCommitId = pushResponse.data.commits[0].commitId;
      log(`✅ Code pushed to ${branchName}, new commit: ${newCommitId}`);
      return newCommitId;
    } catch (error) {
      log(`❌ Code push failed: ${error.message}`, "ERROR");
      throw error;
    }
  }

  async createPullRequest(
    repositoryId,
    repositoryName,
    sourceBranch,
    targetBranch,
    title,
    description
  ) {
    try {
      log(`Creating pull request from ${sourceBranch} to ${targetBranch}`);

      const prTitle = title || `AI Generated Code - ${sourceBranch}`;
      const prDescription =
        description ||
        `This pull request contains AI-generated code.

**Branch:** ${sourceBranch}
**Target:** ${targetBranch}

Please review the generated code before merging.`;

      const pullRequestResponse = await this.adoApi.post(
        `/${encodeURIComponent(this.projectId)}/_apis/git/repositories/${repositoryId}/pullrequests?api-version=7.0`,
        {
          sourceRefName: `refs/heads/${sourceBranch}`,
          targetRefName: targetBranch,
          title: prTitle,
          description: prDescription,
        }
      );

      if (!pullRequestResponse.data?.pullRequestId) {
        throw new Error("Failed to create pull request");
      }

      const pullRequest = pullRequestResponse.data;
      log(`✅ Pull request created: ${pullRequest.pullRequestId}`);

      return {
        pullRequest: pullRequest,
        url:
          pullRequest._links?.web?.href ||
          this.generatePullRequestUrl(
            repositoryName,
            pullRequest.pullRequestId
          ),
      };
    } catch (error) {
      log(`❌ Pull request creation failed: ${error.message}`, "ERROR");
      throw error;
    }
  }

  generatePullRequestUrl(repositoryName, pullRequestId) {
    // Extract organization name from organizationUrl
    let orgName;
    try {
      const url = new URL(this.organizationUrl);
      const pathParts = url.pathname
        .split("/")
        .filter((part) => part.length > 0);
      orgName = pathParts[0]; // Get 'torslev' from '/torslev/'

      if (!orgName) {
        orgName = url.hostname.split(".")[0];
      }
    } catch (error) {
      orgName = "unknown-org";
    }

    // Prevent double encoding by decoding first if already encoded
    let cleanRepoName;
    try {
      cleanRepoName = decodeURIComponent(repositoryName);
    } catch (error) {
      cleanRepoName = repositoryName;
    }

    // Correct Azure DevOps PR URL format: https://dev.azure.com/org/_git/reponame/pullrequest/123
    return `https://dev.azure.com/${orgName}/_git/${encodeURIComponent(cleanRepoName)}/pullrequest/${pullRequestId}`;
  }
}

