// Repository matching logic (same as production)
function findRepositoryMatch(repositories, searchName) {
  log(`Searching for repository: "${searchName}"`);
  log(`Available repositories: ${repositories.map((r) => r.name).join(", ")}`);

  // 1. Exact match
  let targetRepository = repositories.find((repo) => repo.name === searchName);
  if (targetRepository) {
    return { repository: targetRepository, matchType: "exact" };
  }

  // 2. Case-insensitive match
  targetRepository = repositories.find(
    (repo) => repo.name.toLowerCase() === searchName.toLowerCase()
  );
  if (targetRepository) {
    return { repository: targetRepository, matchType: "case-insensitive" };
  }

  // 3. Partial match
  targetRepository = repositories.find(
    (repo) =>
      repo.name.toLowerCase().includes(searchName.toLowerCase()) ||
      searchName.toLowerCase().includes(repo.name.toLowerCase())
  );
  if (targetRepository) {
    return { repository: targetRepository, matchType: "partial" };
  }

  return null;
}

async function testAIJobFlow() {
  log("=== Starting AI Job Flow Repository Name Test ===");

  const testResults = {
    steps: {},
    repositoryNameAnalysis: {
      webhookConfigRepositoryName: CONFIG.repositoryName,
      actualRepositoryName: null,
      matchType: null,
      nameChanged: false,
    },
  };

  try {
    // Setup Azure DevOps API
    const adoApi = axios.create({
      baseURL: CONFIG.organizationUrl,
      headers: {
        Authorization: `Basic ${Buffer.from(`:${CONFIG.pat}`).toString("base64")}`,
        "Content-Type": "application/json",
        Accept: "application/json",
      },
      timeout: 30000,
    });

    // Step 1: Get project
    log("\n--- STEP 1: Project Lookup ---");
    const projectsResponse = await adoApi.get(
      "/_apis/projects?api-version=7.0"
    );
    const project = projectsResponse.data.value.find(
      (p) => p.name.toLowerCase() === CONFIG.projectName.toLowerCase()
    );

    if (!project) {
      throw new Error(`Project '${CONFIG.projectName}' not found`);
    }

    log(`✅ Project found: ${project.name} (ID: ${project.id})`);
    testResults.steps.projectLookup = { success: true, projectId: project.id };

    // Step 2: Simulate AI Job Creation (webhook behavior)
    log("\n--- STEP 2: Simulate Webhook Creating AI Job ---");
    const mockJob = {
      id: "test-job-" + Date.now(),
      projectId: project.id,
      repositoryName: CONFIG.repositoryName, // From ProjectWebhookConfig.repositoryName
      prompt: `Implement: ${CONFIG.workItemTitle}`,
      project: {
        id: project.id,
        name: project.name,
        adoProjectId: project.id,
      },
    };

    log(`✅ Mock AI job created`);
    log(
      `   - Job repository name (from webhook config): "${mockJob.repositoryName}"`
    );
    testResults.steps.aiJobCreation = {
      success: true,
      repositoryNameFromConfig: mockJob.repositoryName,
    };

    // Step 3: Repository Lookup (simulating Step 3 in process-ai-jobs.ts)
    log("\n--- STEP 3: Repository Lookup (job.repositoryName) ---");

    // Get all repositories
    const repositoriesResponse = await adoApi.get(
      `/${encodeURIComponent(project.id)}/_apis/git/repositories?api-version=7.0`
    );

    if (!repositoriesResponse.data?.value) {
      throw new Error("No repositories found");
    }

    const repositories = repositoriesResponse.data.value;
    log(`Found ${repositories.length} repositories in project`);

    // Test repository matching logic
    const matchResult = findRepositoryMatch(
      repositories,
      mockJob.repositoryName
    );

    if (!matchResult) {
      const availableRepos = repositories.map((r) => r.name).join(", ");
      throw new Error(
        `Repository "${mockJob.repositoryName}" not found. Available: ${availableRepos}`
      );
    }

    const { repository, matchType } = matchResult;

    log(`✅ Repository lookup successful`);
    log(`   - Searched for: "${mockJob.repositoryName}"`);
    log(`   - Found: "${repository.name}"`);
    log(`   - Match type: ${matchType}`);
    log(`   - Repository ID: ${repository.id}`);

    const nameChanged = mockJob.repositoryName !== repository.name;
    if (nameChanged) {
      log(`⚠️  REPOSITORY NAME CHANGED!`, "WARN");
      log(
        `   - Original (from webhook config): "${mockJob.repositoryName}"`,
        "WARN"
      );
      log(`   - Actual (from Azure DevOps): "${repository.name}"`, "WARN");
    }

    testResults.steps.repositoryLookup = {
      success: true,
      searchedName: mockJob.repositoryName,
      foundName: repository.name,
      repositoryId: repository.id,
      matchType: matchType,
      nameChanged: nameChanged,
    };

    testResults.repositoryNameAnalysis = {
      webhookConfigRepositoryName: mockJob.repositoryName,
      actualRepositoryName: repository.name,
      matchType: matchType,
      nameChanged: nameChanged,
    };

    // Step 4: Simulate subsequent steps using the found repository name
    log("\n--- STEP 4: Subsequent Processing ---");

    // This is where the issue could occur - which repository name gets used?
    const repositoryNameForSubsequentSteps = repository.name; // This is what the current code does

    log(
      `Repository name used in subsequent steps: "${repositoryNameForSubsequentSteps}"`
    );

    if (nameChanged) {
      log(
        `⚠️  POTENTIAL ISSUE: Subsequent steps will use "${repositoryNameForSubsequentSteps}" instead of "${mockJob.repositoryName}"`,
        "WARN"
      );
    }

    // Step 5: Test branch creation (just get default branch to verify)
    log("\n--- STEP 5: Default Branch Check ---");
    const refsResponse = await adoApi.get(
      `/${encodeURIComponent(project.id)}/_apis/git/repositories/${repository.id}/refs?filter=heads/&api-version=7.0`
    );

    if (refsResponse.data?.value && refsResponse.data.value.length > 0) {
      const branches = refsResponse.data.value;
      let defaultBranch = branches.find(
        (ref) => ref.name === "refs/heads/main"
      );
      if (!defaultBranch) {
        defaultBranch = branches.find(
          (ref) => ref.name === "refs/heads/master"
        );
      }

      if (defaultBranch) {
        log(`✅ Default branch found: ${defaultBranch.name}`);
        testResults.steps.defaultBranchCheck = {
          success: true,
          branchName: defaultBranch.name,
          repositoryUsed: repositoryNameForSubsequentSteps,
        };
      }
    }

    // Final Analysis
    log("\n=== REPOSITORY NAME ANALYSIS ===");
    log(
      `Webhook Config Repository Name: "${testResults.repositoryNameAnalysis.webhookConfigRepositoryName}"`
    );
    log(
      `Actual Azure DevOps Repository Name: "${testResults.repositoryNameAnalysis.actualRepositoryName}"`
    );
    log(`Match Type: ${testResults.repositoryNameAnalysis.matchType}`);
    log(
      `Name Changed During Processing: ${testResults.repositoryNameAnalysis.nameChanged}`
    );

    if (testResults.repositoryNameAnalysis.nameChanged) {
      log(`\n⚠️  REPOSITORY NAME MISMATCH DETECTED!`, "WARN");
      log(
        `This could cause issues if any part of the system expects the original name.`,
        "WARN"
      );
      log(
        `Recommendation: Update ProjectWebhookConfig.repositoryName to match Azure DevOps exactly.`,
        "WARN"
      );
    } else {
      log(`\n✅ Repository names match - no issues detected.`);
    }

    console.log("\n=== TEST RESULTS ===");
    console.log(JSON.stringify(testResults, null, 2));

    return testResults;
  } catch (error) {
    log(`❌ Test failed: ${error.message}`, "ERROR");
    console.log("\n=== ERROR DETAILS ===");
    console.log(error);
    return { success: false, error: error.message, results: testResults };
  }
}

// Run the test
if (require.main === module) {
  testAIJobFlow()
    .then((results) => {
      if (results.success !== false) {
        process.exit(0);
      } else {
        process.exit(1);
      }
    })
    .catch((error) => {
      console.error("Test failed:", error);
      process.exit(1);
    });
}

module.exports = { testAIJobFlow, findRepositoryMatch };
